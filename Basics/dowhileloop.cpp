// WHEN YOU WANT THE LOOP TO EXECUTE A MNIMUM OF ONE TIME, WO PLACETHE DO WHILE LOOP. SO UNLIKE IN WHILE IF THE CONDITION IS FALCE, THE LOO JUST NEVER RUNS, IN DO WHILE THE LOOP WILL RUN ONCE AND THEN CHECK TH CONDITION.
#include <iostream>
using namespace std;

int main()
{

    int i = 15;
    do
    {
        cout << "Siddhi" << "\n";
        i++;
    } while (i <= 10);

    return 0;
}